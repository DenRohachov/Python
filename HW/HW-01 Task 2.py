# Write a program that will calculate the sum,
# difference, product, exponentiation etc. of two numbers
# a and b that the program reads from the console
# (data entered by the user) and will output
# appropriate result:
# "a + b = "  ...
# "a - b = "   ...
# "a * b = "   ...
# "a / b = "   ...
# "a**b = "   ...
# "a//b = "   ...
# "a%b = "  ...


a = int(input("Вкажить значення a "))
b = int(input("Вкажить значення b "))

print("a + b =", a + b)
print("a - b =", a - b)
print("a * b =", a * b)
print("a / b =", a / b)
print("a ** b =", a ** b)
print("a // b =", a // b)
print("a % b =", a % b)